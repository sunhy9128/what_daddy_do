-- 1. 更新 period CHECK 约束，与 pregnancy_stages 表保持一致
-- =====================
-- 先删除旧约束
ALTER TABLE IF EXISTS public.psychological_support
  DROP CONSTRAINT IF EXISTS psychological_support_period_check;
ALTER TABLE IF EXISTS public.preset_items
  DROP CONSTRAINT IF EXISTS preset_items_period_check;

-- 迁移现有 postpartum 细分 period 数据到 postpartum（必须在加新约束前执行）
UPDATE public.psychological_support SET period = 'postpartum' WHERE period LIKE 'postpartum_%';
UPDATE public.preset_items SET period = 'postpartum' WHERE period LIKE 'postpartum_%';

-- 添加新约束，与 pregnancy_stages 表保持一致
ALTER TABLE IF EXISTS public.psychological_support
  ADD CONSTRAINT psychological_support_period_check
  CHECK (period IN (
    'preconception', 'first', 'second', 'third',
    'postpartum'
  ));
ALTER TABLE IF EXISTS public.preset_items
  ADD CONSTRAINT preset_items_period_check
  CHECK (period IN (
    'preconception', 'first', 'second', 'third',
    'postpartum'
  ));

